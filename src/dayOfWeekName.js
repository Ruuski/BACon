dayOfWeekName = (dayOfWeek) => {
  if (dayOfWeek === 1) {
    return 'Monday';
  }
  else if (dayOfWeek === 2) {
    return 'Tuesday';
  }
  else if (dayOfWeek === 3) {
    return 'Wednesday';
  }
  else if (dayOfWeek === 4) {
    return 'Thursday';
  }
  else if (dayOfWeek === 5) {
    return 'Friday';
  }
  else if (dayOfWeek === 6) {
    return 'Saturday';
  }
  else if (dayOfWeek === 7) {
    return 'Sunday';
  }
}

export default dayOfWeekName;
